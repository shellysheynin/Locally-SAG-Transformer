class DistilledVisionTransformer(VisionTransformer):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.dist_token = nn.Parameter(torch.zeros(1, 1, self.embed_dim))
        num_patches = self.patch_embed.num_patches
        self.pos_embed = nn.Parameter(torch.zeros(1, num_patches + 2, self.embed_dim))
        self.head_dist = nn.Linear(self.embed_dim, self.num_classes) if self.num_classes > 0 else nn.Identity()

        trunc_normal_(self.dist_token, std=.02)
        trunc_normal_(self.pos_embed, std=.02)
        self.head_dist.apply(self._init_weights)

    def forward_features(self, x):
        # taken from https://github.com/rwightman/pytorch-image-models/blob/master/timm/models/vision_transformer.py
        # with slight modifications to add the dist_token
        B = x.shape[0]
        x, patches = self.patch_embed(x)

        cls_tokens = self.cls_token.expand(B, -1, -1)  # stole cls_tokens impl from Phil Wang, thanks
        dist_token = self.dist_token.expand(B, -1, -1)
        x = torch.cat((cls_tokens, dist_token, x), dim=1)

        x = x + self.pos_embed
        x = self.pos_drop(x)
        # print("x shape: ", x.shape)
        new_patches = []
        new_patches.append(x.clone().cuda())
        for i in range(patches.shape[0]):

            new_patch = torch.cat((cls_tokens, self.dist_token.expand(x.shape[0], -1, -1), patches[i]), dim=1)
            new_patch = self.pos_drop(new_patch + self.pos_embed)
            new_patches.append(new_patch)
        new_patches = torch.stack(new_patches).permute(1, 2, 0, 3)
        # print("new patches: ", new_patches.shape)
        # print(self.patch_embed.num_patches, self.num_tokens)
        new_patches = new_patches.contiguous().view(x.shape[0], (self.patch_embed.num_patches +2) *
                                                    new_patches.shape[2], self.embed_dim)
        for blk in self.blocks:
            x = blk(x, new_patches)

        x = self.norm(x)
        return x[:, 0], x[:, 1]

    def forward(self, x):
        x, x_dist = self.forward_features(x)
        x = self.head(x)
        x_dist = self.head_dist(x_dist)
        if self.training:
            return x, x_dist
        else:
            # during inference, return the average of both classifier predictions
            return (x + x_dist) / 2

@register_model
def deit_tiny_patch0_cifar10(pretrained=False, **kwargs):
    model =  ShiftingTransformer(
        image_size=44,
        patch_size=2,
        embedding_dim=192,
        heads=3,
        num_hierarchies=3,  # number of hierarchies
        num_layers_per_block=(4, 4, 4),  # the number of transformer blocks at each heirarchy, starting from the bottom
        num_classes=10,
        init_patch_embed_size =1
    )


    model.default_cfg = _cfg(num_classes=10,input_size= (3,32,32))
    print(model.default_cfg)
    if pretrained:
        checkpoint = torch.hub.load_state_dict_from_url(
            url="https://dl.fbaipublicfiles.com/deit/deit_tiny_patch16_224-a1311bcf.pth",
            map_location="cpu", check_hash=True
        )
        model.load_state_dict(checkpoint["model"])
    return model

